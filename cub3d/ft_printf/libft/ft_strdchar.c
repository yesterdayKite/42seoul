#include "libft.h"

char	*ft_strdchar(char src)
{
	char	*a;
	int		i;

	i = 1;
	a = (char *)malloc(sizeof(char) * (i + 1));
	*a = 0;
	a[0] = src;
	a[1] = '\0';
	return (a);
}
